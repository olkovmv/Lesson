package TemperatureConverter;

public class Celsius {
    private double tempCel;

    public Celsius(double tempCel) {
        setTempCel(tempCel);
    }

    public double getTempCel() {
        return tempCel;
    }

    public void setTempCel(double tempCel) {
        this.tempCel = tempCel;
    }
}
